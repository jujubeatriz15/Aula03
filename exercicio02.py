#exercicio02 das 4 operacoes
numero01 = float(input("digite o primeiro numero: "))
numero02 = float(input("digite o segundo numero: "))
adicao = numero01+numero02
subtracao = numero01-numero02
multiplicao = numero01*numero02
divisao = numero01/numero02
print(f"{numero01} + {numero02} = {adicao}")
print(f"{numero01} - {numero02} = {subtracao}")
print(f"{numero01} * {numero02} = {multiplicao}")
print(f"{numero01} / {numero02} = {divisao}")
